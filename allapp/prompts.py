_BASE = """あなたはSNS運用のプロです。以下の条件でコンテンツを作成してください。

## プラットフォーム
{platform_name}

## ネタ・テーマ
{topic}

## 作成する種類
{content_type}

## プラットフォーム別ルール
{rules}
{affiliate_block}{notes_block}
## 出力形式
- そのままコピーして投稿できる完成形で出力する
- 案は2パターン出す (A案 / B案)
- 最後に投稿に最適な時間帯を1行で提案する
"""

_TYPE_EXTRA = {
    "動画台本": (
        "\n## 追加指示 (動画台本)\n"
        "- シーンごとに「映像イメージ / セリフ / テロップ」を表形式で書く\n"
        "- 尺の目安を各シーンに入れる\n"
    ),
    "画像生成プロンプト": (
        "\n## 追加指示 (画像生成)\n"
        "- 画像生成AI (Midjourney / DALL-E等) 用の英語プロンプトを出力する\n"
        "- 構図・ライティング・スタイル指定を含める\n"
        "- 日本語の意図説明も併記する\n"
    ),
    "アフィリエイト記事": (
        "\n## 追加指示 (アフィリエイト)\n"
        "- 売り込み感を出さず、体験談・比較形式で自然に誘導する\n"
        "- 紹介リンクを置く位置を明示する\n"
        "- ステマ規制対応のため PR表記 を必ず入れる\n"
    ),
}


def build_prompt(platform_id: str, platform_name: str, content_type: str,
                 topic: str, affiliate_url: str, notes: str,
                 category: str = "") -> str:
    rules = PLATFORM_RULES.get(
        platform_id,
        CATEGORY_RULES.get(category, "- 一般的なSNSのベストプラクティスに従う"),
    )
    affiliate_block = ""
    if affiliate_url.strip():
        affiliate_block = (
            f"\n## アフィリエイト誘導\n"
            f"以下のリンクへ自然に誘導する構成にする:\n{affiliate_url.strip()}\n"
        )
    notes_block = ""
    if notes.strip():
        notes_block = f"\n## 補足・ターゲット\n{notes.strip()}\n"
    prompt = _BASE.format(
        platform_name=platform_name,
        topic=topic.strip() or "(自由にバズりそうなネタを提案して)",
        content_type=content_type,
        rules=rules,
        affiliate_block=affiliate_block,
        notes_block=notes_block,
    )
    prompt += _TYPE_EXTRA.get(content_type, "")
    return prompt
